// Take two numbers and prints their square and cube.

#include<stdio.h>
int main()
{
    int i, j;
    printf("\n Enter the value of I ");
    scanf("%d", &i);
    printf("\n %d", i*i);
    printf("\n %d", i*i*i);
    printf("\n Enter the value of J ");
    scanf("%d", &j);
    printf("\n %d", j*j);
    printf("\n %d", j*j*j);
    

    return 0;

}

